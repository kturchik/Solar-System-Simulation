        public Text FileName;

        public Text NumberOfIcons;

        public Image[] Images;

        public string[] Dimensions = {
            "16x16",
            "32x32",
            "64x64"
        };

        public string Path = "FontAwesome";

        private int index = -1;

        public int SpriteCount
        {
            get
            {
                return fileNames.Length;
            }
        }

        public void Start()
        {
            if (Images != null && Dimensions != null && Images.Length >= Dimensions.Length)
            {
                Next();
            }
            else
            {
                Debug.LogError("Number of 'Images' must be equals the number of 'Dimensions'!");
            }

            if (NumberOfIcons != null)
                NumberOfIcons.text = "Number of icons: " + fileNames.Length;
        }

        public void Previous()
        {
            if (index > 0)
            {
                index--;
            }
            else
            {
                index = fileNames.Length - 1;
            }

            setImages();
        }

        public void Next()
        {
            if (index < fileNames.Length - 1)
            {
                index++;
            }
            else
            {
                index = 0;
            }

            setImages();
        }

        public void SetIndex(string value)
        {
            int newIndex;

            if (int.TryParse(value, out newIndex))
            {

                if (newIndex > fileNames.Length - 1)
                {
                    index = fileNames.Length - 1;
                }
                else if (newIndex < 0)
                {
                    index = 0;
                }
                else
                {
                    index = newIndex;
                }

                setImages();
            }
        }

        private void setImages()
        {
            for (int ii = 0; ii < Dimensions.Length; ii++)
            {
                string path = Path + "/" + Dimensions[ii] + "/" + fileNames[index] + "_" + Dimensions[ii];
                //Debug.Log(path);
                Images[ii].sprite = Resources.Load<Sprite>(path);
            }

            if (FileName != null)
            {
                FileName.text = index + ": " + fileNames[index];
            }
        }

